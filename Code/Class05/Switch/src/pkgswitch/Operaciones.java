public class Operaciones {
        public double Suma(double valor1,double valor2)
    {
        return valor1 + valor2;
    }
    public double Resta(double valor1, double valor2)
    {
        return valor1-valor2;
    }
    public double División(double valor1, double valor2)
    {
        return valor1/valor2;
    }
    public double Multiplicación(double valor1, double valor2)
    {
        return valor1*valor2;
    }
    public double Raiz(double valor1, double valor2)
    {
        return Math.sqrt(valor1);
    }
    public double Potencia(double valor1, double valor2)
    {
        return Math.pow(valor1, valor2);
    }
    
}
